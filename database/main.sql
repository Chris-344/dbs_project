-- Borrowing procedures with status log tracking
CREATE OR REPLACE PROCEDURE BORROW_PUBLICATION(
    p_pub_id IN NUMBER,
    p_borrower_name IN VARCHAR2,
    p_issue_date IN DATE,
    p_return_date IN DATE,
    p_student_id IN NUMBER DEFAULT NULL,
    p_borrow_id OUT NUMBER
) AS
BEGIN
    -- Insert borrow record
    INSERT INTO BORROWING (PUB_ID, BORROWER_NAME, ISSUE_DATE, RETURN_DATE, STUDENT_ID)
    VALUES (p_pub_id, p_borrower_name, p_issue_date, p_return_date, p_student_id)
    RETURNING BORROW_ID INTO p_borrow_id;
    
    -- Log status
    INSERT INTO BORROW_STATUS_LOG (BORROW_ID, STATUS, NOTES)
    VALUES (p_borrow_id, 'ISSUED', 'Book issued to ' || p_borrower_name);
END;
/

CREATE OR REPLACE PROCEDURE RETURN_PUBLICATION(
    p_borrow_id IN NUMBER
) AS
BEGIN
    UPDATE BORROWING 
    SET RETURNED = 1 
    WHERE BORROW_ID = p_borrow_id;
    
    INSERT INTO BORROW_STATUS_LOG (BORROW_ID, STATUS, NOTES)
    VALUES (p_borrow_id, 'RETURNED', 'Book returned successfully');
END;
/

