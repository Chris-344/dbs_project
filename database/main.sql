CREATE OR REPLACE PROCEDURE ADD_PUBLICATION(
    p_title IN VARCHAR2,
    p_year IN NUMBER,
    p_source IN VARCHAR2,
    p_category_id IN NUMBER DEFAULT NULL,
    p_publisher_id IN NUMBER DEFAULT NULL,
    p_pub_id OUT NUMBER
) AS
BEGIN
    INSERT INTO PUBLICATIONS (TITLE, YEAR, SOURCE, CATEGORY_ID, PUBLISHER_ID)
    VALUES (p_title, p_year, p_source, p_category_id, p_publisher_id)
    RETURNING PUB_ID INTO p_pub_id;
END;
/

