CREATE OR REPLACE FUNCTION GET_PUBLICATION_AVAILABILITY(p_pub_id IN NUMBER)
RETURN NUMBER AS
    v_copies_available NUMBER;
    v_active_borrows NUMBER;
BEGIN
    SELECT COPIES_AVAILABLE INTO v_copies_available 
    FROM PUBLICATIONS 
    WHERE PUB_ID = p_pub_id;
    
    SELECT COUNT(*) INTO v_active_borrows
    FROM BORROWING
    WHERE PUB_ID = p_pub_id 
    AND RETURNED = 0
    AND TRUNC(RETURN_DATE) >= TRUNC(SYSDATE);
    
    RETURN v_copies_available - v_active_borrows;
END;
/

-- Academic evaluation functions: Aggregate queries with GROUP BY
CREATE OR REPLACE FUNCTION GET_MOST_BORROWED_BOOK
RETURN VARCHAR2 AS
    v_title VARCHAR2(200);
BEGIN
    SELECT TITLE INTO v_title
    FROM (
        SELECT p.TITLE, COUNT(b.BORROW_ID) as borrow_count
        FROM PUBLICATIONS p
        LEFT JOIN BORROWING b ON p.PUB_ID = b.PUB_ID
        GROUP BY p.PUB_ID, p.TITLE
        ORDER BY borrow_count DESC
    )
    WHERE ROWNUM <= 1;
    
    RETURN v_title;
END;
/

CREATE OR REPLACE FUNCTION GET_BOOKS_PER_CATEGORY(p_category_id IN NUMBER)
RETURN NUMBER AS
    v_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count
    FROM PUBLICATIONS
    WHERE CATEGORY_ID = p_category_id;
    
    RETURN v_count;
END;
/

CREATE OR REPLACE FUNCTION GET_ACTIVE_BORROWINGS
RETURN NUMBER AS
    v_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count
    FROM BORROWING
    WHERE RETURNED = 0;
    
    RETURN v_count;
END;
/

CREATE OR REPLACE FUNCTION GET_OVERDUE_COUNT
RETURN NUMBER AS
    v_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count
    FROM BORROWING
    WHERE RETURNED = 0 AND TRUNC(RETURN_DATE) < TRUNC(SYSDATE);
    
    RETURN v_count;
END;
/

