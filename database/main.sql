CREATE OR REPLACE VIEW PUBLICATION_DETAILS AS
SELECT 
    p.PUB_ID,
    p.TITLE,
    p.YEAR,
    p.SOURCE,
    c.CATEGORY_NAME,
    pl.NAME as PUBLISHER_NAME,
    pl.LOCATION as PUBLISHER_LOCATION,
    p.COPIES_AVAILABLE,
    (SELECT COUNT(*) FROM BORROWING b WHERE b.PUB_ID = p.PUB_ID AND b.RETURNED = 0) as ACTIVE_BORROWS,
    (SELECT AVG(RATING) FROM REVIEWS r WHERE r.PUB_ID = p.PUB_ID) as AVG_RATING
FROM PUBLICATIONS p
LEFT JOIN CATEGORIES c ON p.CATEGORY_ID = c.CATEGORY_ID
LEFT JOIN PUBLISHERS pl ON p.PUBLISHER_ID = pl.PUBLISHER_ID;

CREATE OR REPLACE VIEW BORROWING_DETAILS AS
SELECT 
    b.BORROW_ID,
    b.BORROWER_NAME,
    s.NAME as STUDENT_NAME,
    s.DEPARTMENT,
    p.TITLE,
    c.CATEGORY_NAME,
    b.ISSUE_DATE,
    b.RETURN_DATE,
    CASE 
        WHEN b.RETURNED = 1 THEN 'RETURNED'
        WHEN TRUNC(SYSDATE) > TRUNC(b.RETURN_DATE) THEN 'OVERDUE'
        ELSE 'ACTIVE'
    END as BORROW_STATUS,
    ABS(TRUNC(SYSDATE) - TRUNC(b.RETURN_DATE)) as DAYS_FROM_DUE
FROM BORROWING b
LEFT JOIN STUDENTS s ON b.STUDENT_ID = s.STUDENT_ID
JOIN PUBLICATIONS p ON b.PUB_ID = p.PUB_ID
LEFT JOIN CATEGORIES c ON p.CATEGORY_ID = c.CATEGORY_ID;

CREATE OR REPLACE VIEW CATEGORY_STATISTICS AS
SELECT 
    c.CATEGORY_ID,
    c.CATEGORY_NAME,
    COUNT(DISTINCT p.PUB_ID) as BOOK_COUNT,
    COUNT(DISTINCT b.BORROW_ID) as TOTAL_BORROWS,
    (SELECT COUNT(*) FROM BORROWING b2 WHERE b2.PUB_ID IN 
        (SELECT PUB_ID FROM PUBLICATIONS WHERE CATEGORY_ID = c.CATEGORY_ID) 
        AND b2.RETURNED = 0) as ACTIVE_BORROWS
FROM CATEGORIES c
LEFT JOIN PUBLICATIONS p ON c.CATEGORY_ID = p.CATEGORY_ID
LEFT JOIN BORROWING b ON p.PUB_ID = b.PUB_ID
GROUP BY c.CATEGORY_ID, c.CATEGORY_NAME;

CREATE OR REPLACE VIEW STUDENT_BORROWING_HISTORY AS
SELECT 
    s.STUDENT_ID,
    s.NAME,
    s.EMAIL,
    s.DEPARTMENT,
    COUNT(DISTINCT b.BORROW_ID) as TOTAL_BORROWS,
    COUNT(DISTINCT CASE WHEN b.RETURNED = 0 THEN b.BORROW_ID END) as ACTIVE_BORROWS,
    COUNT(DISTINCT CASE WHEN TRUNC(SYSDATE) > TRUNC(b.RETURN_DATE) AND b.RETURNED = 0 THEN b.BORROW_ID END) as OVERDUE_COUNT
FROM STUDENTS s
LEFT JOIN BORROWING b ON s.STUDENT_ID = b.STUDENT_ID
GROUP BY s.STUDENT_ID, s.NAME, s.EMAIL, s.DEPARTMENT;
