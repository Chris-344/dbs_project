-- ══════════════════════════════════════════════════════════════
-- CORE TABLES (ORIGINAL)
-- ══════════════════════════════════════════════════════════════

CREATE TABLE AUTHORS (
    AUTHOR_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(100) NOT NULL,
    INSTITUTION VARCHAR2(150) NOT NULL,
    DEPARTMENT VARCHAR2(150) NOT NULL,
    EMAIL VARCHAR2(100),
    ADDRESS VARCHAR2(200),
    HOMEPAGE VARCHAR2(200)
);

-- ══════════════════════════════════════════════════════════════
-- LOOKUP TABLES (NEW - NORMALIZATION)
-- ══════════════════════════════════════════════════════════════

CREATE TABLE CATEGORIES (
    CATEGORY_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CATEGORY_NAME VARCHAR2(100) NOT NULL UNIQUE,
    DESCRIPTION VARCHAR2(500)
);

CREATE TABLE PUBLISHERS (
    PUBLISHER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(150) NOT NULL UNIQUE,
    LOCATION VARCHAR2(150),
    EMAIL VARCHAR2(100),
    PHONE VARCHAR2(15)
);

CREATE TABLE STUDENTS (
    STUDENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(100) NOT NULL,
    EMAIL VARCHAR2(100) UNIQUE,
    DEPARTMENT VARCHAR2(100),
    ENROLLMENT_DATE DATE DEFAULT SYSDATE
);

-- ══════════════════════════════════════════════════════════════
-- PUBLICATIONS TABLE (MODIFIED - WITH FOREIGN KEYS)
-- ══════════════════════════════════════════════════════════════

CREATE TABLE PUBLICATIONS (
    PUB_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TITLE VARCHAR2(200) NOT NULL,
    YEAR NUMBER(4),
    SOURCE VARCHAR2(200),
    CATEGORY_ID NUMBER REFERENCES CATEGORIES(CATEGORY_ID),
    PUBLISHER_ID NUMBER REFERENCES PUBLISHERS(PUBLISHER_ID),
    COPIES_AVAILABLE NUMBER DEFAULT 1,
    ISBN VARCHAR2(20),
    PAGES NUMBER,
    CREATED_AT TIMESTAMP DEFAULT SYSDATE
);

CREATE TABLE AUTHOR_PUBLICATION (
    AUTHOR_ID NUMBER REFERENCES AUTHORS(AUTHOR_ID),
    PUB_ID NUMBER REFERENCES PUBLICATIONS(PUB_ID),
    PRIMARY KEY (AUTHOR_ID, PUB_ID)
);

-- ══════════════════════════════════════════════════════════════
-- BORROWING TABLE (ORIGINAL - WITH OPTIONAL STUDENT LINK)
-- ══════════════════════════════════════════════════════════════

CREATE TABLE BORROWING (
    BORROW_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PUB_ID NUMBER REFERENCES PUBLICATIONS(PUB_ID),
    STUDENT_ID NUMBER REFERENCES STUDENTS(STUDENT_ID),
    BORROWER_NAME VARCHAR2(100) NOT NULL,
    ISSUE_DATE DATE NOT NULL,
    RETURN_DATE DATE NOT NULL,
    RETURNED NUMBER DEFAULT 0,
    CREATED_AT TIMESTAMP DEFAULT SYSDATE
);

-- ══════════════════════════════════════════════════════════════
-- AUDIT & TRANSACTION TABLES (NEW - ADVANCED TRACKING)
-- ══════════════════════════════════════════════════════════════

CREATE TABLE BORROW_STATUS_LOG (
    LOG_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BORROW_ID NUMBER REFERENCES BORROWING(BORROW_ID),
    STATUS VARCHAR2(50) NOT NULL,
    STATUS_DATE TIMESTAMP DEFAULT SYSDATE,
    NOTES VARCHAR2(200)
);

CREATE TABLE REVIEWS (
    REVIEW_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PUB_ID NUMBER REFERENCES PUBLICATIONS(PUB_ID),
    STUDENT_ID NUMBER REFERENCES STUDENTS(STUDENT_ID),
    RATING NUMBER(2,1) CHECK (RATING >= 1 AND RATING <= 5),
    COMMENT VARCHAR2(500),
    REVIEW_DATE TIMESTAMP DEFAULT SYSDATE
);

