CREATE TABLE AUTHORS (
    AUTHOR_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(100) NOT NULL,
    INSTITUTION VARCHAR2(150) NOT NULL,
    DEPARTMENT VARCHAR2(150) NOT NULL,
    EMAIL VARCHAR2(100),
    ADDRESS VARCHAR2(200),
    HOMEPAGE VARCHAR2(200)
);

CREATE TABLE PUBLICATIONS (
    PUB_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TITLE VARCHAR2(200) NOT NULL,
    YEAR NUMBER(4),
    SOURCE VARCHAR2(200),
    PAGE_FROM NUMBER,
    PAGE_TO NUMBER
);

CREATE TABLE AUTHOR_PUBLICATION (
    AUTHOR_ID NUMBER REFERENCES AUTHORS(AUTHOR_ID),
    PUB_ID NUMBER REFERENCES PUBLICATIONS(PUB_ID),
    PRIMARY KEY (AUTHOR_ID, PUB_ID)
);

-- Indexes for search optimization
CREATE INDEX idx_author_name ON AUTHORS(NAME);
CREATE INDEX idx_author_institution ON AUTHORS(INSTITUTION);
CREATE INDEX idx_author_department ON AUTHORS(DEPARTMENT);
CREATE INDEX idx_publication_title ON PUBLICATIONS(TITLE);

CREATE OR REPLACE PROCEDURE ADD_PUBLICATION(
    p_title IN VARCHAR2,
    p_year IN NUMBER,
    p_source IN VARCHAR2,
    p_page_from IN NUMBER,
    p_page_to IN NUMBER,
    p_pub_id OUT NUMBER
) AS
BEGIN
    INSERT INTO PUBLICATIONS (TITLE, YEAR, SOURCE, PAGE_FROM, PAGE_TO)
    VALUES (p_title, p_year, p_source, p_page_from, p_page_to)
    RETURNING PUB_ID INTO p_pub_id;
END;
/

CREATE OR REPLACE PROCEDURE ADD_AUTHOR(
    p_name IN VARCHAR2,
    p_institution IN VARCHAR2,
    p_department IN VARCHAR2,
    p_email IN VARCHAR2,
    p_address IN VARCHAR2,
    p_homepage IN VARCHAR2,
    p_author_id OUT NUMBER
) AS
BEGIN
    INSERT INTO AUTHORS (NAME, INSTITUTION, DEPARTMENT, EMAIL, ADDRESS, HOMEPAGE)
    VALUES (p_name, p_institution, p_department, p_email, p_address, p_homepage)
    RETURNING AUTHOR_ID INTO p_author_id;
END;
/
CREATE OR REPLACE PROCEDURE LINK_AUTHOR_PUBLICATION(
    p_author_id IN NUMBER,
    p_pub_id IN NUMBER
)
AS
BEGIN
    INSERT INTO AUTHOR_PUBLICATION (AUTHOR_ID, PUB_ID)
    VALUES (p_author_id, p_pub_id);
END;
/

